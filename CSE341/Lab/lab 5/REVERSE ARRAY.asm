.MODEL SMALL
.STACK 100H
.DATA
; declare variables here 
ARR1 DB 10 DUP(0) 
ARR2 DB 10 DUP(0)
X DB "ENTER YOUR ARRAY: $"
Y DB 10,13,"SECOND ARRAY: $"
.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX
; enter your code here      

LEA DX,X
MOV AH,9
INT 21H

MOV CX,10
MOV SI,0
START:
MOV AH,1
INT 21H
SUB AL,30H
MOV ARR1[SI],AL
ADD SI,1
LOOP START

LEA DX,Y
MOV AH,9
INT 21H

MOV CX,10
MOV SI,0
MOV DI,9
STR:
MOV AL,ARR1[DI]
MOV ARR2[SI],AL
ADD SI,1
SUB DI,1
LOOP STR


MOV CX,10
MOV SI,0
PRINT:
MOV DL,ARR2[SI]
ADD DL,30H
MOV AH,2
INT 21H
ADD SI,1
LOOP PRINT

;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN