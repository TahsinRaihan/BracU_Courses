.MODEL SMALL
.STACK 100H
.DATA
A db 100DUP(0)
X DB "ENTER YOUR NAMES LENGTH: $"
Y DB 10,13, "ENTER ALPHABETS: $"
Z DB 10,13, "YOUR NAME: $"
.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX
; enter your code 

LEA DX,X
MOV AH,9
INT 21H

MOV AH,1
INT 21H
SUB AL,30H
MOV BL,AL 

LEA DX,Y
MOV AH,9
INT 21H

MOV CL,0
MOV SI,0
LOOP1:
CMP CL,BL
JGE LOOP2  
MOV AH,1
INT 21H 
SUB AL,30H
MOV A[SI],AL
ADD SI,1
ADD CL,1
JMP LOOP1

LOOP2:

LEA DX,Z
MOV AH,9
INT 21H

MOV CL,0
MOV SI,0 
START:
CMP CL,BL
JGE END 
MOV DL,A[SI]
ADD DL,30H
MOV AH,2
INT 21H
ADD SI,1
ADD CL,1
JMP START

END:
HLT
 
 
 
 
 
 
;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN