.MODEL SMALL
.STACK 100H
.DATA
; declare variables here 
X DB "ENTER A NUMBER: $" 
Y DB 10,13,"MAX NUMBER IS $"
Z DB 10,13,"MIN NUMBER IS $"
NUM1 DB 0
NUM2 DB 0
NUM3 DB 0

.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX
; enter your code here
  
LEA DX,X
MOV AH,9
INT 21H

MOV AH,1
INT 21H
SUB AL,30H
MOV BL,AL
MOV NUM1,BL 

MOV AH,1
INT 21H
SUB AL,30H
MOV BH,AL
MOV NUM2,BH

MOV AH,1
INT 21H
SUB AL,30H
MOV CL,AL
MOV NUM3,CL

CMP BL,BH
JGE MAX1
MOV BL,BH
MAX1:
CMP BL,CL
JGE MAX
MOV BL,CL

MAX:
LEA DX,Y 
MOV AH,9
INT 21H
MOV DL,BL
ADD DL,30H
MOV AH,2
INT 21H 


MOV BL,NUM1
MOV BH,NUM2
MOV CL, NUM3

CMP BL,BH
JLE MIN1
MOV BL,BH

MIN1:
CMP BL,CL
JLE MIN
MOV BL,CL 

MIN:
LEA DX,Z 
MOV AH,9
INT 21H
MOV DL,BL
ADD DL,30H
MOV AH,2
INT 21H 




END:
HLT








;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN