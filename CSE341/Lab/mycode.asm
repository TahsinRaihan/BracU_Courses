.MODEL SMALL
 
.STACK 100H

.DATA
 NUM DB 1
 NUM1 DB 2
 NUM2 DB 3
 NUM3 DB 5

.CODE
MAIN PROC

; initialize DS

MOV AX,@DATA
MOV DS,AX 

MOV AL, NUM
MOV AH,NUM1
MOV BL,NUM2
MOV BH,NUM3
ADD AL,AH
DEC BL
MUL BL
MOV DX,AX
ADD BH,BL
ADD BH,NUM2
MOV AL,NUM
MOV AH, NUM1
MUL AH
SUB BH,AL
MOV AX,DX
DIV BH

 








 

;exit to DOS
               
MOV AX,4C00H
INT 21H

MAIN ENDP
    END MAIN
