.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'ENTER YOUR PASSWORD: $'
MSG1 DB 10,13, 'YOUR PASSWORD: $'
NUM DB '* $'

.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG
MOV AH,9
INT 21H

MOV CX,5
START:
MOV AH,1
INT 21H
LOOP START 

LEA DX,MSG1
MOV AH,9
INT 21H

MOV CX,5
LAST:
MOV DL,NUM
MOV AH,2
INT 21H
LOOP LAST


;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN