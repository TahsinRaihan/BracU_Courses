.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "ENTER FIRST NUMBER: $"
MSG2 DB 10,13,"RESULT: $" 
MSG3 DB "EVEN NUMBER $"
MSG4 DB "ODD NUMBER $"
.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX

LEA DX, MSG1
MOV AH,9
INT 21H

MOV AH,1
INT 21H 
SUB AL ,30H
MOV CL,AL  ;INPUT1

LEA DX, MSG2
MOV AH,9
INT 21H

MOV AH,0
MOV AL,CL
MOV BL,2
DIV BL

CMP AH,0
JE EVEN
JNE ODD


EVEN:
LEA DX, MSG3
MOV AH,9
INT 21H
JMP EXIT

ODD:
LEA DX, MSG4
MOV AH,9
INT 21H
JMP EXIT









EXIT:
HLT
;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN