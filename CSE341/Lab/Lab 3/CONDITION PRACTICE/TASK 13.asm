.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "USER INPUT: $"
MSG2 DB 10,13,"OUTPUT: $"
DAY1 DB 'SATURDAY$'
DAY2 DB 'SUNDAY$'
DAY3 DB 'MONDAY$'
DAY4 DB 'TUESDAYDAY$'
DAY5 DB 'WEDNESDAY$'
DAY6 DB 'THURSDAY$'
DAY7 DB 'FRIDAY$'

NUM DB 0

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

LEA DX, MSG1
MOV AH,9
INT 21H


MOV AH,1
INT 21H   
SUB AL,30H
MOV BL,AL   ;input 1        

LEA DX, MSG2
MOV AH,9
INT 21H



CMP BL,1
JE SATURDAY
CMP BL,2
JE SUNDAY 
CMP BL,3
JE MONDAY
CMP BL,4
JE TUESDAY
CMP BL,5
JE WEDNESDAY
CMP BL,6
JE THURSDAY
CMP BL,7
JE FRIDAY




SATURDAY:
LEA DX, DAY1
MOV AH,9
INT 21H
JMP EXIT 

SUNDAY:
LEA DX, DAY2
MOV AH,9
INT 21H
JMP EXIT

MONDAY:
LEA DX, DAY3
MOV AH,9
INT 21H
JMP EXIT

TUESDAY:
LEA DX, DAY4
MOV AH,9
INT 21H
JMP EXIT

WEDNESDAY:
LEA DX, DAY5
MOV AH,9
INT 21H
JMP EXIT

THURSDAY:
LEA DX, DAY6
MOV AH,9
INT 21H
JMP EXIT

FRIDAY:
LEA DX, DAY7
MOV AH,9
INT 21H
JMP EXIT




EXIT:
HLT

MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN
