.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "ENTER FIRST NUMBER: $"
MSG2 DB 10,13,"ENTER SECOND NUMBER: $"
MSG3 DB 10,13,"RESULT: $"
.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX

LEA DX, MSG1
MOV AH,9
INT 21H

MOV AH,1
INT 21H
MOV CL,AL  ;INPUT1

LEA DX, MSG2
MOV AH,9
INT 21H

MOV AH,1
INT 21H
MOV CH,AL  ;INPUT2

LEA DX, MSG3
MOV AH,9
INT 21H

CMP CL,CH
JLE FIRST
JG LAST

FIRST:
MOV DL,CL
MOV AH,2
INT 21H
JMP EXIT

LAST:
MOV DL,CH
MOV AH,2
INT 21H
JMP EXIT









EXIT:
HLT
;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN