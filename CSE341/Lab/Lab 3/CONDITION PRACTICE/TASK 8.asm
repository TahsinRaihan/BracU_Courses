.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "ENTER CHARACTER: $"
MSG2 DB 10,13,"RESULT: $" 
MSG3 DB "VOWEL $"
MSG4 DB "CONSONENT $"
.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX

LEA DX, MSG1
MOV AH,9
INT 21H

MOV AH,1
INT 21H 
MOV CL,AL  ;INPUT1

LEA DX, MSG2
MOV AH,9
INT 21H

CMP CL,65
JE VOWEL
CMP CL,69
JE VOWEL
CMP CL,73
JE VOWEL
CMP CL,79
JE VOWEL
CMP CL,85
JE VOWEL

CMP CL,97
JE VOWEL
CMP CL,101
JE VOWEL
CMP CL,105
JE VOWEL
CMP CL,111
JE VOWEL
CMP CL,117
JE VOWEL

JMP CONSONENT

VOWEL:
LEA DX, MSG3
MOV AH,9
INT 21H
JMP EXIT

CONSONENT:
LEA DX, MSG4
MOV AH,9
INT 21H
JMP EXIT









EXIT:
HLT
;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN