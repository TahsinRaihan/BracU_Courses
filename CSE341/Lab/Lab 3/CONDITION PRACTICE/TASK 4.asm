.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "ENTER FIRST NUMBER: $"
MSG2 DB 10,13,"RESULT: $"
.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX

LEA DX, MSG1
MOV AH,9
INT 21H

MOV AH,1
INT 21H
MOV CL,AL
SUB CL,30H  ;INPUT1

LEA DX, MSG2
MOV AH,9
INT 21H

CMP CL,1
JE FIRST
CMP CL,3
JE FIRST
CMP CL,2
JE LAST
CMP CL,4
JE LAST




FIRST:
MOV DL,"o"
MOV AH,2
INT 21H
JMP EXIT

LAST:
MOV DL,"e"
MOV AH,2
INT 21H
JMP EXIT

EXIT:
HLT
;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN