.MODEL SMALL
.STACK 100H
.DATA
MSG db 'Enter numbers : $' 

MSG1 DB 10,13,'MAXIMUM NUMBER IS : $'

MSG2 DB 10,13,'MINIMUM NUMBER IS : $'

NUM1 DB 0
NUM2 DB 0
NUM3 DB 0
.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG
MOV AH,9
INT 21H

MOV AH,1
INT 21H
SUB AL,30H
MOV BL,AL
MOV NUM1,BL

MOV AH,1
INT 21H
SUB AL,30H
MOV BH,AL
MOV NUM2,BH

MOV AH,1
INT 21H
SUB AL,30H
MOV CL,AL
MOV NUM3,CL

CMP BL,BH
JGE MAX1
MOV BL,BH

MAX1:

CMP BL,CL
JGE MAX
MOV BL,CL

MAX:

LEA DX, MSG1
MOV AH,9
INT 21H

MOV DL,BL
ADD DL,30H
MOV AH,2
INT 21H



MOV BL,NUM1
MOV BH,NUM2
MOV CL,NUM3


CMP BL,BH
JLE MIN1
MOV BL,BH

MIN1:

CMP BL,CL
JLE MIN
MOV BL,CL

MIN:

LEA DX, MSG2
MOV AH,9
INT 21H

MOV DL,BL
ADD DL,30H
MOV AH,2
INT 21H


JMP EXIT







EXIT: 
HLT





;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN