.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "ENTER CHARACTER: $"
MSG2 DB 10,13,"RESULT: $"
MSG3 DB "NOT AN UPPERCASE CHARACTER $"
.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX

LEA DX, MSG1
MOV AH,9
INT 21H

MOV AH,1
INT 21H
MOV CL,AL;INPUT1

LEA DX, MSG2
MOV AH,9
INT 21H


CMP CL,64
JG UPPER

UPPER:
CMP CL,91
JG LOWER
MOV DL,CL
MOV AH,2
INT 21H
JMP EXIT

LOWER:
LEA DX, MSG3
MOV AH,9
INT 21H
JMP EXIT









EXIT:
HLT
;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN