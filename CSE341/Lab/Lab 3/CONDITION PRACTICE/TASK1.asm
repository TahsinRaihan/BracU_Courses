.MODEL SMALL
.STACK 100H
.DATA

MSG1 DB 'Enter a number: $'
MSG2 DB 0DH,0AH,'Result: $'

.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX 

LEA DX, MSG1
MOV AH,9
INT 21H

MOV AH,1
INT 21H 
MOV CL,AL
CMP AL, "-"
JE NEXT_INPUT
JNE POSITIVE

NEXT_INPUT:
MOV AH,1
INT 21H 
LEA DX, MSG2
MOV AH,9
INT 21H 
MOV DL,5
ADD DL,30H
MOV AH,2
INT 21H
JMP EXIT

POSITIVE:
LEA DX, MSG2
MOV AH,9
INT 21H  
SUB CL,30H
MOV DL,CL
ADD DL,30H
MOV AH,2
INT 21H
JMP EXIT



EXIT:
HLT


;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN



