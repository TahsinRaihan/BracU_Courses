.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "USER INPUT: $"
MSG2 DB 10,13,"OUTPUT: $"
DAYS31 DB '31 day$'
DAYS30 DB '30 day$'
DAYS28 DB '28 day$'
NUM DB 0

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

LEA DX, MSG1
MOV AH,9
INT 21H


MOV AH,1
INT 21H   
SUB AL,30H
MOV BL,AL   ;input 1        


MOV AH,1
INT 21H
CMP AL,0DH
JE SINGLE_DIGIT; if Enter pressed after first goes to single digit 


SUB AL,30H
MOV CL,AL           
MOV AL,BL
MOV BL,10
MUL BL
MOV NUM,AL              
ADD NUM,CL           
JMP CHECK_MONTH

SINGLE_DIGIT:
MOV AL,BL
MOV NUM,AL

CHECK_MONTH:
LEA DX, MSG2
MOV AH,9
INT 21H

MOV AL,NUM

CMP AL,1
JE NORMAL
CMP AL,3
JE NORMAL
CMP AL,5
JE NORMAL
CMP AL,7
JE NORMAL
CMP AL,8
JE NORMAL
CMP AL,10
JE NORMAL
CMP AL,12
JE NORMAL

CMP AL,4
JE LESS
CMP AL,6
JE LESS
CMP AL,9
JE LESS
CMP AL,11
JE LESS

CMP AL,2
JE FEBRUARY


NORMAL:
LEA DX, DAYS31
MOV AH,9
INT 21H
JMP EXIT

LESS:
LEA DX, DAYS30
MOV AH,9
INT 21H
JMP EXIT

FEBRUARY:
LEA DX, DAYS28
MOV AH,9
INT 21H
JMP EXIT


EXIT:
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN
